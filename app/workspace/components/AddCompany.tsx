const postCompany = async ({ name, email, user }: any) => {
  const supabase = createClient();

  const { data, error } = await supabase
    .from("company")
    .insert([{ name: name, email: email, user: user }])
    .select();
  if (data) {
    console.log(data);
  } else {
    console.log("error:", error);
  }
};

